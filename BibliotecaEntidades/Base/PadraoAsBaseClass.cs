        protected override void internalSave(ref IWTPostgreNpgsqlCommand command)
        {
            try
            {
                if (this.ID != -1)
                {
                    command.CommandText =
                        "UPDATE  " +
                        "  public.padrao_as   " +
                        "SET  " + 
                        "  pas_kit = :pas_kit, " + 
                        "  pas_conteudo = :pas_conteudo, " + 
                        "  entity_uid = :entity_uid, " + 
                        "  version = :version "+
                        "WHERE  " +
                        "  id_padrao_as = :id " +
                        "RETURNING id_padrao_as;";
                }
                else
                {
                    command.CommandText =
                        "INSERT INTO " +
                        "public.padrao_as " +
                        "( " +
                        "  pas_kit , " + 
                        "  pas_conteudo , " + 
                        "  entity_uid , " + 
                        "  version  "+
                        ")  " +
                        "VALUES ( " +
                        "  :pas_kit , " + 
                        "  :pas_conteudo , " + 
                        "  :entity_uid , " + 
                        "  :version  "+
                        ")RETURNING id_padrao_as;";
                }

                command.Parameters.Clear();
                command.Parameters.Add(new IWTPostgreNpgsqlCommandParameter("id", NpgsqlDbType.Integer));
                command.Parameters[command.Parameters.Count - 1].Value = this.ID;
                command.Parameters.Add(new IWTPostgreNpgsqlCommandParameter("pas_kit", NpgsqlDbType.Varchar));
                command.Parameters[command.Parameters.Count - 1].Value = (object)this.Kit ?? DBNull.Value;
