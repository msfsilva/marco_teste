                           whereClause += " OR UPPER(mini_fabrica.entity_uid) LIKE :buscaCompletaUpper ";
                           whereClause += " OR LOWER(mini_fabrica.entity_uid) LIKE :buscaCompletaLower ";
                        }
                        whereClause += ") ";
