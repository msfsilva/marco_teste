                     }
                     break;
                     case "sta_description":
                     case "Description":
                     switch (parametro.TipoOrdenacao)
                     {
                        case TipoOrdenacao.Numerica:
                        case TipoOrdenacao.Data:
                           orderByClause += " , status.sta_description " + parametro.Ordenacao.ToString().ToUpper(); 
